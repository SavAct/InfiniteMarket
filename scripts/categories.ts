export function categoryBigInt(main: number, sub1: number) {
  let byteArray = new Uint8Array([main, sub1, 0, 0, 0, 0, 0, 0]);
  let dataView = new DataView(byteArray.buffer);
  return dataView.getBigUint64(0);
}

export function categoryHex(main: number, sub1: number) {
  return '0x' + categoryBigInt(main, sub1).toString(16).padStart(16, '0');
}

export function categoryPath(main: number, sub1: number) {
  return `${categories[main].name}/${categories[main].child[sub1].name}`;
}

export function categoryName(main: number, sub1: number) {
  return categories[main].child[sub1].name;
}

export function categoryIndexesByBigInt(bigInt: bigint) {
  let byteArray = new Uint8Array(8);
  let dataView = new DataView(byteArray.buffer);
  dataView.setBigUint64(0, bigInt);
  let main = dataView.getUint8(0);
  let sub1 = dataView.getUint8(1);
  return { main, sub1 };
}
